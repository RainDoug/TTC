        <h2>Classes</h2>
        <div class="container">
          <img src="images/tap.jpg" alt="Tap Dance">
          <h3>Tap</h3>
          <h4>Tap your way to success!</h4>
          <p>At the beginner's level, these classes are designed to teach students the rhythm, coordination, terminology, and choreography of tap. Allowing students to develop proper skills and structure needed to further their abilities.</p>
          <p>More advanced classes are designed to refine their basic tap skills, while learning new steps and combinations.  Focusing on faster and more intricate footwork, the student will be able to develop the speed and styles needed for competition.</p>
        </div><!-- end #classes .container -->
        <div class="container">
          <img src="images/ballet.jpg" alt="Ballet Dance">
          <h3>Ballet</h3>
          <h4>Poise and beauty!</h4>
          <p>At the beginner's level, students will learn ballet fundamentals and terminology while developing strength, confidence, balance, and flexibility. With traditional ballet training, students learn how concentration and endurance are essential to perfect their ballet techniques.</p>
          <p>As students advance, classes will focus on complex combinations and routines. Students will take their fundamental ballet moves and maneuver them to become precise and elegant. These classes also help students develop strength and grace to outshine their competition.</p>
        </div><!-- end #classes .container -->
        <div class="container">
          <img src="images/tumbling.jpg" alt="Tumbling">
          <h3>Tumbling</h3>
          <h4>Learn to flip!</h4>
          <p>The tumbling classes start with students leaning the importance of strength and flexibility in tumbling. They will start off with some basic tumbling exercises to gain a better understanding of tumbling.</p>
          <p>Once students have refined their basic skills, students will be prepared for more complex tumbling passes and routines. The skills acquired through tumbling classes can help strengthen a student's tap, ballet, and cheer abilities.</p>
        </div><!-- end #classes .container -->
        <div class="container">
          <img src="images/cheerleading.jpg" alt="Cheerleading">
          <h3>Cheerleading</h3>
          <h4>Show us your spirit!</h4>
          <p>These classes start with students learning motion placement, jumps, and chants. Once these skills are developed, they will be combined so that students can learn basic cheers and routines.</p>
          <p>Our advanced classes are designed for those students looking to try out for the cheerleading team. These students will learn sharper movements and lifts while strengthening their jumps and chants, giving them an inside look at how these tryouts will be held and preparing them for when they make the team.</p>
